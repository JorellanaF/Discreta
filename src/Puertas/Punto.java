package Puertas;

public class Punto {
    private float x,y;

    //Constructor
    public Punto(float x, float y) {
        this.x = x;
        this.y = y;
    }

    //Setter y Getter
    public float getX() {

        return x;
    }

    public void setX(float x) {

        this.x = x;
    }

    public float getY() {

        return y;
    }

    public void setY(float y) {

        this.y = y;
    }
}